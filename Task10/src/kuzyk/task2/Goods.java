package kuzyk.task2;

/**
 * The type Goods.
 */
public class Goods {
    private double cost;
    private String name;

    /**
     * Set cost.
     *
     * @param cost the cost
     */
    public void setCost(double cost){
        this.cost = cost;
    }

    /**
     * Get cost double.
     *
     * @return the double
     */
    public double getCost(){
        return cost;
    }

    /**
     * Set name.
     *
     * @param name the name
     */
    public void setName(String name){
        this.name = name;
    }

    /**
     * Get name string.
     *
     * @return the string
     */
    public String getName(){
        return name;
    }
}
